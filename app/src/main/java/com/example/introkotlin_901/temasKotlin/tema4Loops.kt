package com.example.introkotlin_901.temasKotlin

//for
fun main(){
for(number in 1..5){
print(number)
}
    val nombres = listOf("Arath","Mario","Sam")
    for (nombre in nombres){
        println(nombre)
    }
    //while do-while
    var x=0
    while (x < 5){
        println(x)
        x++
    }
    do{
        println(x)
        x++
    }while(x < 5)

}